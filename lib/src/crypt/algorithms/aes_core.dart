// Constantes de round (Rcon) — potências de x em GF(2^8)
const List<int> _rcon = [
  0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36,
];

// ---------------------------------------------------------------------------
// Aritmética em GF(2^8) com polinômio irredutível x^8+x^4+x^3+x+1
// ---------------------------------------------------------------------------

/// Multiplicação por 2 em GF(2^8) — xtime.
int _xtime(int a) => ((a << 1) ^ ((a & 0x80) != 0 ? 0x1b : 0)) & 0xff;

/// Multiplicações usadas em InvMixColumns.
int _m9(int a)  { final x2 = _xtime(a); return _xtime(_xtime(x2)) ^ a; }
int _m11(int a) { final x2 = _xtime(a); return _xtime(_xtime(x2)) ^ x2 ^ a; }
int _m13(int a) { final x2 = _xtime(a); return _xtime(_xtime(x2)) ^ _xtime(x2) ^ a; }
int _m14(int a) { final x2 = _xtime(a); final x4 = _xtime(x2); return _xtime(x4) ^ x4 ^ x2; }

// ---------------------------------------------------------------------------
// Operações SubWord e RotWord para key schedule
// ---------------------------------------------------------------------------

int _rotWord(int w) => ((w << 8) | (w >>> 24)) & 0xffffffff;

int _subWord(int w) =>
    (_sbox[(w >> 24) & 0xff] << 24) |
    (_sbox[(w >> 16) & 0xff] << 16) |
    (_sbox[(w >> 8) & 0xff] << 8) |
    _sbox[w & 0xff];

// ---------------------------------------------------------------------------
// Expansão de chave (Key Schedule) — FIPS 197 §5.2
// ---------------------------------------------------------------------------

/// Expande a chave [key] (16 ou 32 bytes) para a lista de round keys.
///
/// Retorna:
/// - 176 bytes para AES-128 (11 round keys × 16 bytes)
/// - 240 bytes para AES-256 (15 round keys × 16 bytes)
Uint8List aesExpandKey(Uint8List key) {
  assert(key.length == 16 || key.length == 32,
      'AES: chave deve ter 16 ou 32 bytes.');

  final nk = key.length >> 2;         // palavras na chave (4 ou 8)
  final nr = nk + 6;                   // rounds (10 ou 14)
  final totalWords = 4 * (nr + 1);     // total de palavras expandidas
  final w = List<int>.filled(totalWords, 0);

  // Copia a chave original como primeiras Nk palavras
  for (int i = 0; i < nk; i++) {
    w[i] = (key[i * 4] << 24) |
            (key[i * 4 + 1] << 16) |
            (key[i * 4 + 2] << 8) |
            key[i * 4 + 3];
  }

  for (int i = nk; i < totalWords; i++) {
    int temp = w[i - 1];
    if (i % nk == 0) {
      temp = _subWord(_rotWord(temp)) ^ (_rcon[i ~/ nk - 1] << 24);
    } else if (nk == 8 && i % nk == 4) {
      temp = _subWord(temp);
    }
    w[i] = w[i - nk] ^ temp;
  }

  // Serializa como bytes big-endian
  final result = Uint8List(totalWords * 4);
  for (int i = 0; i < totalWords; i++) {
    result[i * 4]     = (w[i] >> 24) & 0xff;
    result[i * 4 + 1] = (w[i] >> 16) & 0xff;
    result[i * 4 + 2] = (w[i] >> 8) & 0xff;
    result[i * 4 + 3] =  w[i] & 0xff;
  }
  return result;
}

/// Número de rounds a partir do tamanho da chave expandida.
int aesNumRounds(Uint8List expandedKey) =>
    (expandedKey.length ~/ 16) - 1; // 10 para AES-128, 14 para AES-256

// ---------------------------------------------------------------------------
// Transformações do estado (estado em column-major: state[row + col*4])
// ---------------------------------------------------------------------------

void _addRoundKey(Uint8List s, Uint8List ek, int round) {
  final off = round * 16;
  for (int i = 0; i < 16; i++) {
    s[i] ^= ek[off + i];
  }
}

void _subBytes(Uint8List s) {
  for (int i = 0; i < 16; i++) {
    s[i] = _sbox[s[i]];
  }
}

void _invSubBytes(Uint8List s) {
  for (int i = 0; i < 16; i++) {
    s[i] = _sboxInv[s[i]];
  }
}

void _shiftRows(Uint8List s) {
  // Linha 1: rotação esquerda por 1
  int t = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = t;
  // Linha 2: rotação esquerda por 2
  t = s[2]; s[2] = s[10]; s[10] = t;
  t = s[6]; s[6] = s[14]; s[14] = t;
  // Linha 3: rotação esquerda por 3 (= direita por 1)
  t = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = t;
}

void _invShiftRows(Uint8List s) {
  // Linha 1: rotação direita por 1
  int t = s[13]; s[13] = s[9]; s[9] = s[5]; s[5] = s[1]; s[1] = t;
  // Linha 2: rotação direita por 2 (idêntica a esquerda por 2)
  t = s[2]; s[2] = s[10]; s[10] = t;
  t = s[6]; s[6] = s[14]; s[14] = t;
  // Linha 3: rotação direita por 3 (= esquerda por 1)
  t = s[3]; s[3] = s[7]; s[7] = s[11]; s[11] = s[15]; s[15] = t;
}

void _mixColumns(Uint8List s) {
  for (int c = 0; c < 4; c++) {
    final o = c << 2;
    final s0 = s[o], s1 = s[o+1], s2 = s[o+2], s3 = s[o+3];
    final x0 = _xtime(s0), x1 = _xtime(s1), x2 = _xtime(s2), x3 = _xtime(s3);
    s[o]   = x0 ^ x1 ^ s1 ^ s2 ^ s3;
    s[o+1] = s0 ^ x1 ^ x2 ^ s2 ^ s3;
    s[o+2] = s0 ^ s1 ^ x2 ^ x3 ^ s3;
    s[o+3] = x0 ^ s0 ^ s1 ^ s2 ^ x3;
  }
}

void _invMixColumns(Uint8List s) {
  for (int c = 0; c < 4; c++) {
    final o = c << 2;
    final s0 = s[o], s1 = s[o+1], s2 = s[o+2], s3 = s[o+3];
    s[o]   = _m14(s0) ^ _m11(s1) ^ _m13(s2) ^ _m9(s3);
    s[o+1] = _m9(s0)  ^ _m14(s1) ^ _m11(s2) ^ _m13(s3);
    s[o+2] = _m13(s0) ^ _m9(s1)  ^ _m14(s2) ^ _m11(s3);
    s[o+3] = _m11(s0) ^ _m13(s1) ^ _m9(s2)  ^ _m14(s3);
  }
}

// ---------------------------------------------------------------------------
// Cifra de bloco AES — FIPS 197 §5.1 e §5.3
// ---------------------------------------------------------------------------

/// Cifra um bloco de 16 bytes [block] usando a chave expandida [expandedKey].
///
/// [expandedKey] deve ser gerado por [aesExpandKey].
Uint8List aesEncryptBlock(Uint8List block, Uint8List expandedKey) {
  assert(block.length == 16, 'AES: bloco deve ter 16 bytes.');
  final nr = aesNumRounds(expandedKey);
  final s = Uint8List.fromList(block);

  _addRoundKey(s, expandedKey, 0);
  for (int r = 1; r < nr; r++) {
    _subBytes(s);
    _shiftRows(s);
    _mixColumns(s);
    _addRoundKey(s, expandedKey, r);
  }
  // Round final (sem MixColumns)
  _subBytes(s);
  _shiftRows(s);
  _addRoundKey(s, expandedKey, nr);
  return s;
}

/// Decifra um bloco de 16 bytes [block] usando a chave expandida [expandedKey].
///
/// [expandedKey] deve ser gerado por [aesExpandKey].
Uint8List aesDecryptBlock(Uint8List block, Uint8List expandedKey) {
  assert(block.length == 16, 'AES: bloco deve ter 16 bytes.');
  final nr = aesNumRounds(expandedKey);
  final s = Uint8List.fromList(block);

  _addRoundKey(s, expandedKey, nr);
  for (int r = nr - 1; r >= 1; r--) {
    _invShiftRows(s);
    _invSubBytes(s);
    _addRoundKey(s, expandedKey, r);
    _invMixColumns(s);
  }
  // Round final (sem InvMixColumns)
  _invShiftRows(s);
  _invSubBytes(s);
  _addRoundKey(s, expandedKey, 0);
  return s;
}
